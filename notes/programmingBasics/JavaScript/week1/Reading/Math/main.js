
    // modulus - remainder
    console.log (5 % 2) // would print 1

    // rounding
    var num1 = Math.round(2.5) // num1 is 3
    num1 = Math.round(2.4)     // num1 is 2
    num1 = Math.round(-2.1)    // num1 is -2
    num1 = Math.round(-2.9)    // num1 is -3

    // round down
    var num2 = Math.floor(2.9)  //num2 is 2

    // round up
    var num3 = Math.ceil(2.1)   //num3 is 3

    // truncate - delete decimal off, no rounding occurs
    var num4 = Math.trunc(2.8)  //num4 is 2
    num4 = Math.trunc(-2.2)     //num4 is -2